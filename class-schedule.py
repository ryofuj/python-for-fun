# Define your sets
data = {
  "a": {"s": [1, 2, 3]},
  "b": {"s": [1, 2]},
  "c": {"s": [1, 2, 3, 4, 5, 6]}
}


# Extract the lists of 's' values for each key
s_lists = [data[key]['s'] for key in data.keys()]

# Initialize a list to store the combinations
combinations = []

# Recursive function to generate combinations
def generate_combinations(current_combination, current_index):
    if current_index == len(s_lists):
        combinations.append(list(current_combination))
        return
    for value in s_lists[current_index]:
        current_combination.append(value)
        generate_combinations(current_combination, current_index + 1)
        current_combination.pop()

# Start generating combinations
generate_combinations([], 0)

# Print all generated combinations
for combination in combinations:
    print(combination)
